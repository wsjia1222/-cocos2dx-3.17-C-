#pragma once

/**
 * CardModel - 卡牌数据模型
 * 
 * 这个结构体用来存储一张卡牌的所有数据信息
 * 它是数据层（Model层）的基础，只存储数据，不包含任何显示逻辑
 * 
 * 使用场景：
 * - 在GameModel中存储所有卡牌的数据
 * - 在回退功能中保存卡牌的状态
 * - 在视图层创建CardView时传递卡牌信息
 */
struct CardModel {
    int id;         // 卡牌的唯一标识符，每张卡牌都有不同的ID，用于区分不同的卡牌
    int face;       // 卡牌的点数：1=A(尖), 2-10=数字, 11=J(钩), 12=Q(后), 13=K(王)
    int suit;       // 卡牌的花色：0=梅花♣, 1=方块♦, 2=红桃♥, 3=黑桃♠
    bool isFaceUp;  // 卡牌是否翻开：true=正面朝上(能看到点数), false=背面朝上(看不到点数)
    float posX, posY; // 卡牌在屏幕上的位置坐标(x, y)，用于记录卡牌应该显示在哪里
};

